altura = float(input("Digite sua altura:"))
altura_minima = float(input("Digite a altura mínima para entrar em um dos brinquedos do parque de diversões:"))

if altura >= altura_minima:
    print("Você tem altura suficiente para andar no brinquedo.")
else:
    print("Você não possui altura suficiente para andar no brinquedo.")